<h2>conditions d'utilisation des chemins</h2>

<h3>article L. 361-1 du code de l'environnement :</h3>

  <p>La circulation des piétons sur les voies et chemins inscrits au  
plan départemental des itinéraires de promenade et de randonnée, ou  
ceux identifiés pour les chemins privés, après conventions passées  
avec les propriétaires de ces chemins, par les communes et les  
fédérations de randonneurs agréées s'effectue librement, dans le  
respect des lois et règlements de police et des droits des riverains.</p>
    <p>Les maires, en vertu de leur pouvoir de police, peuvent, le cas  
échéant, réglementer les conditions d'utilisation de ces itinéraires.</p>
    <p>La responsabilité civile des propriétaires ruraux et forestiers 
ne saurait être engagée au titre des dommages causés ou subis à  
l'occasion de la circulation des piétons ou de la pratique  
d'activités de loisirs qu'en raison de leurs actes fautifs.</p>

<h3>N.B.</h3>
<p>Le CRE Limousin assure la mise en ligne d'informations qui lui sont  
transmises par les concepteurs et gestionnaires d'itinéraires de  
randonnées. il n'est pas responsable de l'accessibilité de ces  
itinéraires et de leur état d'entretien.
